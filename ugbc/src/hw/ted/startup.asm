TEDSTARTUP:

    ; SET_VIDEO( DEFAULT_VIDEO )
    LDA $FF12
    AND #%00000111
    ORA #%00011000
    STA $FF12
    LDA $FF14
    AND #%00000111
    LDA #%01011000
    STA $FF14
    LDA $FF13
    AND #%00000011
    LDA #%11010100
    STA $FF13
    
    ; SET_BACKGROUND_COLOR( BLACK )
    LDA #$0
    STA $FF15

    ; SET STARTUP CURSOR POSITION    
    LDA #0
    STA $64
    LDA #0
    STA $66

    RTS